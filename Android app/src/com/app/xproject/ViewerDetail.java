package com.app.xproject;

public class ViewerDetail
{
  String viewer_id;
  String viewer_name;
  String photo;
  String service_name;
  String latitude;
  String longitude;

  public String getViewerID()	{
	  return viewer_id;
  }
  
  public String getViewerName()	{
	  return viewer_name;
  }
  
  public String getPhoto()	{
	  return photo;
  }
  
  public String getServiceName()	{
	return service_name;
  }
  
  public String getLatitude()	{
	  return latitude;
  }
  
  
  public String getLongtitude()	{
	  return longitude;
  }
}